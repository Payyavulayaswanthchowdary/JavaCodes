package Task20;

public class VowelOrConsonant {
    public static void main(String[] args) {
        char ch = 'a';
        if(ch == 'a' || ch == 'e' || ch == 'i' || ch == 'o' || ch == 'u'){
            System.out.println("it's an vowel");
        }
        else{
            System.out.println("it's a consonant");
        }
    }
}
