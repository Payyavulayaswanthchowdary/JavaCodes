package Strings;

public class Concat {
    public static void main(String[] args) {
        String s ="yaswa";
        String s1 = "jaswa";
        System.out.println(s.concat(s1));

        int a = 10;
        int b = 20;
        String s2 = String.valueOf(a);
        String s3 = String.valueOf(b);
        System.out.println(s2+s3);
    }
}
